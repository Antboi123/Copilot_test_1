// Simple "Debug the Bug" coding mini-game for kids
// Find and fix the bug in this code!

function countToFive() {
  let result = "";
  for (let i = 1; i <= 5; i++) {
    // Oops! There's a bug here, can you fix it?
    result += i + ",";
  }
  return result;
}

